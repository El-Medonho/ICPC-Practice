signed solve(){
    
    bool ok = true;
    int n; cin >> n;
    string s; cin >> s;
    int last = 0, cnt = 0;;
    for(char c: s){
        if(c-'0' < last) ok = false;
        if(c-'0' == 0) cnt++;
        last = c-'0'; 
    }

    if(ok){
        cout << "Bob\n";
        return 0;
    }

    vector<int> ans(0);
    ok = true;
    for(int i = 0; i < n; i++){
        if(i < cnt && s[i] == '1') ans.push_back(i+1);
        if(i >= cnt) ok = false;
        if(!ok && cnt > 0 && s[i] == '0') {ans.push_back(i+1); cnt--;}
    }

    cout << "Alice" << endl;
    cout << ans.size() << endl;
    for(int i: ans) cout << i << ' ';
    cout << endl;


    return 0;
}

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}