signed solve(){
    
    int n; cin >> n;

    string s; cin >> s;
    int cnt = 0;
    int x0 = 0, x1 = 0;
    int y0 = 0, y1 = 0;
    char last = '(';
    for(char c: s){
        if(c == '('){
            if(c != last) cnt++;
            if(cnt == 0) x0++;
            if(cnt == 1) x1++;
        }
        if(c == ')'){
            if(cnt == 0) y0++;
            if(cnt == 1) y1++;
        }
        last = c;
    }

    if(cnt > 1 || (cnt == 1 && x1 > 1)){
        cout << n-2 << endl;
    }
    else cout << -1 << endl;


    return 0;
}

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}