signed solve(){
    
    int n; cin >> n;
    vector<int> a(n), b(n), c(n);

    for(int &i: a) cin >> i;
    for(int &i: b) cin >> i;
    for(int &i: c) cin >> i;

    vector<int> offsetj(n, 1);

    for(int j = 0; j < n; j++){
        for(int k = 0; k < n; k++){
            int h = k-j;
            if(h < 0) h += n;
            if(c[k] <= b[j]) offsetj[h] = 0;
        }
    }

    vector<int> offseti(n, 1);
    for(int i = 0; i < n; i++){
        for(int j = 0; j < n; j++){
            int h = j-i;
            if(h < 0) h+= n;
            if(b[j] <= a[i]) offseti[h] = 0;
        }
    }

    ll cnti = 0, cntj = 0;

    for(int x: offseti) cnti += x;
    for(int x: offsetj) cntj += x;

    cnti *= cntj;
    cnti *= n;

    cout << cnti << endl;


    return 0;
}

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}