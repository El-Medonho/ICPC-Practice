#define endl "\n"
typedef long long ll;
typedef unsigned long long ull;
typedef long double ld;
typedef tree<int,null_type, less<int>, rb_tree_tag, tree_order_statistics_node_update> ordered_set;
typedef tree<int,null_type, less_equal<int>, rb_tree_tag, tree_order_statistics_node_update> multiordered_set;  //--set.lower_bound(value) ao inves de find

mt19937 rng((int) std::chrono::steady_clock::now().time_since_epoch().count());

const int mod = 998244353;
const ll inf = 1e18+5;

vector<ll> fat(55, 1);

ll expo(ll b, ll e){
    ll res=1;
    while(e){
        if(e&1) res=res*b%mod;
        b=b*b%mod;
        e >>= 1;
    }
    return res;
}


signed solve(){
    
    int n; cin >> n;
    ll b; cin >> b;
    vector<ll> arr(n);
    for(ll &i: arr) cin >> i;

    ll mx = (*max_element(arr.begin(), arr.end()));

    ll qtt = 0;
    ll diff = 0;

    for(int i = 0; i < n; i++){
        if(arr[i] == mx) qtt++;
        else diff += mx - arr[i];
    }

    if(diff - (n-qtt) > b) {
        cout << 0  << endl;
        return 0;
    }

    ll sobra = b - (diff - (n-qtt));
    sobra = min(sobra, n-qtt);

    ll range = sobra + qtt;

    ll ans = fat[range] * expo(fat[range - qtt] * fat[qtt] % mod, mod-2) % mod;
    ans = ans * fat[qtt] % mod;
    ans = ans * fat[n-qtt] % mod;

    cout << ans << endl;


    return 0;
}

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    for(int i = 2; i < 55; i++) fat[i] = fat[i-1] * i % mod;

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}