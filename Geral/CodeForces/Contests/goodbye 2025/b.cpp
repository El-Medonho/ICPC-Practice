signed solve(){
    
    string s; cin >> s;

    int cnt = 0;

    if(s[0] == 'u'){
        s[0] = 's'; cnt++;
    }
    int n = s.size();

    if(s[n-1] == 'u'){
        s[n-1] = 's'; cnt++;
    }

    int cc = 0;

    for(char c: s){
        if(c == 'u') cc++;
        else {
            if(cc > 1) cnt += (cc)/2;
            cc = 0;
        }
    }

    cout << cnt << endl;


    return 0;
}

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}