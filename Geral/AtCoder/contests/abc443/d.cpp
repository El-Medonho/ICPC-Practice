const int mod = 1e9+7;
const ll inf = 1e18+5;

signed solve(){
    int n; cin >> n;

    priority_queue<pair<int,int>> pq;
    vector<int> mx(n, 0), vis(n, 0);
    for(int i = 0; i < n; i++){
        int x; cin >> x;
        x = n-x;
        mx[i] = x;
        pq.emplace(x, i);
    }

    ll ans = 0;

    while(!pq.empty()){
        auto[h, i] = pq.top(); pq.pop();
        if(vis[i]) continue;
        vis[i] = 1;
        if(i != 0 && mx[i-1] < mx[i]-1){
            ans += mx[i]-1-mx[i-1];
            mx[i-1] = mx[i]-1;
            pq.emplace(mx[i-1], i-1);
        } 

        if(i != n-1 && mx[i+1] < mx[i]-1){
            ans += mx[i]-1-mx[i+1];
            mx[i+1] = mx[i]-1;
            pq.emplace(mx[i+1], i+1);
        } 
    }

    cout << ans << endl;
    


    return 0;
}

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}