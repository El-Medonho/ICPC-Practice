using pii = pair<int,int>;

const int inf = 1e9;

signed solve(){

    int n; cin >> n;

    if(n < 10){
        cout << n << endl;
        return 0;
    }

    vector<int> pre_comp(n+5, 1);
    for(int i = 1; i <= n+2; i++) pre_comp[i] = pre_comp[i-1]*10%n;
    
    vector<vector<pii>> state(n+1, vector<pii>(10, {inf, inf})), last(n+1, vector<pii>(10, {inf, inf}));

    queue<pair<pii,int>> pq;

    for(int i = 1; i <= min(9, n); i++){
        state[i][i] = {1, i};
        pq.emplace(make_pair(1, i), i);
    }

    while(!pq.empty()){
        auto[h, cc] = pq.front(); pq.pop();
        auto[digits, l] = h;
        if(state[cc][l].first < digits) continue;

        int nd = digits+1;
        for(int j = 1; j <= l; j++){
            int nx = (pre_comp[digits]*j + cc) % n;
            if(state[nx][j].first <= nd) continue;

            state[nx][j] = {nd, j};
            last[nx][j] = {cc, l};
            pq.emplace(make_pair(nd, j), nx);
        }
    }

    int b = inf;
    pii sel = {-1, -1};

    for(int i = 1; i < min(10, n); i++){
        if(state[0][i].first < b) {sel = {0, i}; b = state[0][i].first; }
    }

    string ans = "";
    if(sel.first == -1){
        cout << -1 << endl;
        return 0;
    }
    while(sel != make_pair(inf, inf)){
        ans += '0' + sel.second;
        sel = last[sel.first][sel.second];
    }


    cout << ans << endl;


    return 0;
}

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int t = 1;
    // cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}