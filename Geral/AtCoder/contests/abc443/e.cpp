const ll inf = 1e18+5;

signed solve(){
    int n,c; cin >> n >> c;
    vector<string> s(n);
    for(string &i: s) cin >> i;
    c--;

    vector<vector<int>> vis(n, vector<int>(n, 0));
    vector<int> min_occ(n, -10), lib(n, 0);
    vis[n-1][c] = 1; lib[c] = 1;

    for(int i = 0; i < n; i++){
        for(int j = 0; j < n; j++){
            if(s[i][j] == '#') min_occ[j] = max(min_occ[j], i);
        }
    }

    for(int i = n-1; i > 0; i--){
        for(int j = 0; j < n; j++){
            if(vis[i][j]){
                for(int k = j-1; k <= j+1; k++){
                    if(k < 0 || k >= n) continue;
                    if(s[i-1][k] == '#' && (min_occ[k] >= i && !lib[k])) continue;

                    vis[i-1][k] = 1;
                    if(i > min_occ[k]) lib[k] = 1;
                }
            }
        }
    }

    for(int i: vis[0]) cout << i;
    cout << endl;


    return 0;
}

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int t = 1;
    cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}