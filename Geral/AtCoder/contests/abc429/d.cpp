signed solve(){
    
    ll n,m,c; cin >> n >> m >> c;

    vector<ll> save(n);
    set<ll> st;
    
    for(ll i = 0; i < n; i++){
        ll x; cin >> x; 
        save[i] = x;
        st.insert(x);
    }
    
    ll mm = m;
    ll l = -1;
    map<ll,ll> mp;
    m = 0;
    for(ll x: st){
        mp[x] = m++;
    }
    
    vector<ll> mul(m, 0);
    for(ll x: st){
        if(l != -1){
            mul[mp[x]-1] = x-l;
        }
        l = x;
    }
    mul[m-1] = mm - (l - (*mp.begin()).first);
    
    vector<ll> arr(m, 0), sum(2*m+2, 0);

    for(ll i = 0; i < n; i++){
        arr[mp[save[i]]]++;
    }

    for(ll i = 0; i < 2*m+1; i++){
        if(i) sum[i] = sum[i-1];
        sum[i] += arr[i%m];
    }

    ll base = sum[0], ind = 0;

    ll ans = 0;

    for(ll i = 0; i <= 2*m; i++){
        while(sum[i] - base >= c){
            ans += (sum[i] - base) * mul[ind];
            ind++; base = sum[ind];
            if(ind == m) break;
        }
        if(ind == m) break;
    }

    cout << ans << endl;


    return 0;
}

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    ll t = 1;
    // cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}