signed solve(){
    
    int n,m; cin >> n >> m;
    vector<vector<int>> graph(n);

    for(int i = 0; i < m; i++){
        int a,b; cin >> a >> b; a--; b--;
        graph[a].push_back(b);
        graph[b].push_back(a);
    }

    vector<int> ans(n, 0);

    vector<vector<int>> vis(n, vector<int>(2, -1)), d(n, vector<int>(2, -1));

    string s; cin >> s;

    queue<pair<int,int>> qw;

    for(int i = 0; i < n; i++){
        if(s[i] == 'S') {
            qw.emplace(i, 0);
            vis[i][0] = i; d[i][0] = 0;
        }
    }

    while(!qw.empty()){
        auto[cc, it] = qw.front(); qw.pop();

        // cout << cc << ' ' << it << ' ' << vis[cc][it] << ' ' << d[cc][it] << endl;

        if(s[cc] == 'D') {
            ans[cc] += d[cc][it];
        }

        for(int j: graph[cc]){
            if(d[j][1] != -1 || vis[j][0] == vis[cc][it]) continue;
            
            int i = 0;
            if(d[j][0] != -1) i = 1;

            d[j][i] = d[cc][it]+1;
            vis[j][i] = vis[cc][it];
            qw.emplace(j, i);
        }
    }

    for(int i = 0; i < n; i++){
        if(s[i] == 'D') cout << ans[i] << endl;
    }


    return 0;
}

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int t = 1;
    // cin >> t;
    while(t--){
        solve();
    }    

    return 0;
}