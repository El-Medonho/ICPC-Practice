#include "bits/stdc++.h"

using namespace std;

#define endl '\n'

typedef long long ll;

const ll INFL = 1e18;

struct Line{
    mutable ll k, m, p;
    bool operator<(const Line& o) const {return k<o.k;}
    bool operator<(ll x) const {return p<x;}
};

struct LineContainer : multiset<Line, less<>> {
    ll div(ll a, ll b){
        return a/b-((a^b)<0 && a%b);
    }

    int issect(iterator x, iterator y){
        if(y == end()) {x->p=INFL; return 0;}
        if(x -> k==y->k) x->p=x->m > y->m? INFL: -INFL;
        else x->p=div(y->m - x->m, x->k - y->k);
        return x->p >= y->p;
    }

    void add(ll k, ll m){
        auto z = insert({-k,-m,0}), y=z++, x=y;
        while(issect(y, z))z = erase(z);
        if(x != begin() && issect(--x, y)) issect(x, y=erase(y));
        while((y=x) != begin() && (--x)->p >= y->p) issect(x, erase(y));
    }

    ll query(ll x){
        assert(!empty());
        auto l=*lower_bound(x);
        return l.k*x+l.m;
    }
};

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int n; cin >> n;
    ll x; cin >> x;
    LineContainer lc; lc.add(x, 0);

    ll ans = 0;
    vector<ll> s(n), f(n);
    for(ll &i: s) cin >> i;
    for(ll &i: f) cin >> i;

    for(int i = 0; i < n; i++){
        ll q = -lc.query(s[i]);
        ans = q;
        lc.add(f[i], q);
    }

    cout << ans << endl;

    return 0;
}