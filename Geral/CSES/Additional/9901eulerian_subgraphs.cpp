#include "bits/stdc++.h"

using namespace std;

#define endl '\n'

typedef long long ll;

const int mod = 1e9+7;

ll expo(ll b, ll e){
    ll ans = 1;
    while(e){
        if(e&1) ans = ans*b%mod;
        b=b*b%mod;
        e>>=1;
    }
    return ans;
}

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int n,m; cin >> n >> m;

    vector<vector<int>> graph(n);
    for(int i = 0; i < m; i++){
        int a,b; cin >> a >> b; a--; b--;
        graph[a].push_back(b);
        graph[b].push_back(a);
    }

    vector<int> vis(n, 0);
    int cnt = 0;
    for(int i = 0; i < n; i++){
        if(vis[i]) continue;
        cnt++;
        queue<int> qw; qw.push(i);
        vis[i] = 1;
        while(!qw.empty()){
            int cc = qw.front(); qw.pop();
            for(int j: graph[cc]){
                if(vis[j]) continue;
                vis[j] = 1;
                qw.push(j);
            }
        }
    }

    cout << expo(2, m-n+cnt) << endl;

    return 0;
}