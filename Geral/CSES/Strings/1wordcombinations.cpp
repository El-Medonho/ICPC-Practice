// ⠜⠋⢠⣷⢻⣿⣿⣶⣾⣿⣿⣿⣿⠿⣛⣥⣾⣿⠿⠟⠛⠉⠄⠄
 
#include "bits/stdc++.h"
 
using namespace std;
 
#define endl '\n'
 
typedef long long ll;
typedef unsigned long long ull;
 
const int mod = 1e9+7;
 
const int mod2 = 998244353;
 
const ull p1 = 31, p2 = 71;
 
signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);
 
    string s; cin >> s;
    int n = s.size();
 
    unordered_map<ull, ll> mp;
 
    vector<ull> fat(n+10, 1);
    for(int i = 1; i <= n+5; i++){
        fat[i] = fat[i-1] * p1 % mod2;
    }
 
    int k; cin >> k;
    for(int i = 0; i < k; i++){
        string t; cin >> t;
        if(t.size() > n) continue;
 
        pair<ull,ull> ini = {0, 0};
        for(int j = 0; j < t.size(); j++){
            ini.first += (t[j]-'a'+1) * fat[j+1];
            ini.second += (t[j]-'a'+1) * fat[j+1];
 
            ini.first %= mod2;
        }
        ull num = ini.first + ini.second * mod2;
        mp[num]++;
    }
 
    vector<ll> dp(n+1, 0LL); dp[0] = 1;
 
    for(int i = 0; i < n; i++) {
        pair<ull,ull> ini = {0, 0};
        for(int j = 0; i+j < n; j++){
            ini.first += (s[i+j]-'a'+1) * fat[j+1];
            ini.second += (s[i+j]-'a'+1) * fat[j+1];
 
            ini.first %= mod2;
            ull num = ini.first + ini.second * mod2;
 
            if(mp.find(num) != mp.end()){
                dp[i+j+1] += dp[i] * mp[num];
                dp[i+j+1] %= mod;
            }
        }
    }
 
    cout << dp[n] << endl;
 
    
 
    return 0;
}