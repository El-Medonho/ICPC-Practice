#include "bits/stdc++.h"

using namespace std;

#define endl '\n'

typedef long long ll;

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int n, m, k; cin >> n >> m >> k;

    vector<vector<int>> graph(n);
    queue<int> qw;
    vector<int> dist(n, 1e9+9), ans(n, -1), repr(n, -1);

    for(int i = 0; i < k; i++){
        int x; cin >> x; x--;
        dist[x] = 0;
        qw.push(x);
        repr[x] = x;
    }

    for(int i = 0; i < m; i++){
        int a,b; cin >> a >> b; a--; b--;
        graph[a].push_back(b);
        graph[b].push_back(a);
    }

    while(!qw.empty()){
        int cc = qw.front(); qw.pop();
        int d = dist[cc];

        for(int j: graph[cc]){
            if(repr[j] != repr[cc] && repr[j] != -1){
                if(ans[repr[j]] == -1 || ans[repr[j]] > d + dist[j] + 1) ans[repr[j]] = d + dist[j] + 1;
                if(ans[repr[cc]] == -1 || ans[repr[cc]] > d + dist[j] + 1) ans[repr[cc]] = d + dist[j] + 1;
                continue;
            }
            if(d+1 < dist[j]){
                dist[j] = d+1;
                ans[j] = d+1;
                repr[j] = repr[cc];
                qw.push(j);
            }
        }
    }

    for(int x: ans){
        cout << x << ' ';
    }
    cout << endl;

    return 0;
}