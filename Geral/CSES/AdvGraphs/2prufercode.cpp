#include "bits/stdc++.h"

using namespace std;

#define endl '\n'

typedef long long ll;

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int n; cin >> n;

    vector<int> arr(n-2);
    vector<int> cnt(n, 0);
    priority_queue<int, vector<int>, greater<int>> pq;
    for(int &i: arr){
        cin >> i; i--;
        cnt[i]++;
    }
    for(int i = 0; i < n; i++){
        if(cnt[i] == 0) pq.push(i);
    }
    vector<pair<int,int>> ans(0);

    for(int x: arr){
        int cc = pq.top(); pq.pop();
        ans.emplace_back(x, cc);
        cnt[x]--;
        if(cnt[x] == 0) pq.push(x);
    }

    int p = pq.top(); pq.pop();
    int q = pq.top();
    ans.emplace_back(p, q);

    for(auto[a, b]: ans){
        cout << a+1 << ' ' << b+1 << endl;
    }

    return 0;
}