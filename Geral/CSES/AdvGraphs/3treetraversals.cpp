#include "bits/stdc++.h"

using namespace std;

#define endl '\n'

typedef long long ll;

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int n; cin >> n;
    set<int> st;
    
    for(int i = 0; i < n; i++) st.insert(i);
    vector<int> arr(n), left(n);
    vector<int> vis(n, 0);
    for(int &i: arr) {cin >> i; i--;}
    for(int &i: left) {cin >> i; i--;}

    vector<pair<int,int>> graph(n, {-1, -1});

    int l_ind = 0;
    auto dfs = [&](auto&& self, int cc, int last) -> void{
        st.erase(cc);
        vis[arr[cc]] = 1;
        int i = 0;
        if(arr[cc] == left[l_ind]) {l_ind++; i++;}
        if(l_ind == n) return;
        if(vis[left[l_ind]]) return; 

        for(; i < 2; i++){
            int nxt = (*st.upper_bound(cc));
            if(i == 0) graph[cc].first = nxt;
            else graph[cc].second = nxt;
            self(self, nxt, cc);
            if(l_ind == n) return;
            if(arr[cc] == left[l_ind]) l_ind++;
            if(l_ind == n) return;
            if(vis[left[l_ind]]) return; 
        }
    };

    dfs(dfs, 0, 0);

    auto dfs2 = [&](auto&& self, int cc, int last) -> void{
        if(graph[cc].first != -1) self(self, graph[cc].first, cc);
        if(graph[cc].second != -1) self(self, graph[cc].second, cc);
        cout << arr[cc]+1 << ' ';
    };
    
    dfs2(dfs2, 0, 0);
    cout << endl;

    return 0;
}