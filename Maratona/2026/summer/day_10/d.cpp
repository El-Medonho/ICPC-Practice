#include "bits/stdc++.h"

using namespace std;

// #define endl '\n'

typedef long long ll;

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int n,m; cin >> n >> m;
    vector<vector<int>> graph(n);
    for(int i = 0; i < m; i++){
        int a,b; cin >> a >> b; a--; b--;
        graph[a].push_back(b);
        graph[b].push_back(a);
    }

    vector<vector<int>> ans(0);
    vector<int> vis(n, 0), is(n, 0);
    ans.reserve(m);

    auto dfs = [&](auto&& self, int cc, int last) -> int {
        int will = 1;
        vis[cc] = 1; is[cc] = 1;
        vector<int> available(0);
        for(int j: graph[cc]){
            if(vis[j] && is[j] && last != j) available.push_back(j);
            if(vis[j]) continue;
            int is = self(self, j, cc);
            if(is) available.push_back(j);
        }

        if(available.size() % 2 && cc != last){
            will = 0;
            ans.push_back({last, cc, available.back()});
        }

        for(int i = 0; i < ((int)available.size()-1); i+=2){
            ans.push_back({available[i], cc, available[i+1]});
        }

        is[cc] = 0;
        return will;
    };

    for(int i = 0; i < n; i++){
        if(vis[i] == 0) dfs(dfs, i, i);
    }

    cout << ans.size() << '\n';
    for(auto arr: ans){
        for(int x: arr) cout << (x+1) << ' ';
        cout << '\n';
    }

    return 0;
}