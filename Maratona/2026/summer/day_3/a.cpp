#include "bits/stdc++.h"

using namespace std;

#define endl '\n'

typedef long long ll;

signed solve(){

    int x, y, n, m, k, s; cin >> x >> y >> n >> m >> k >> s; s--;
    vector<int> dp(x+1, 0); dp[x] = 1<<k;
    
    int mnt = x;

    vector<int> a(n), b(m);
    
    for(int &i: a) cin >> i;
    for(int &i: b) {cin >> i; mnt = min(mnt, i);}
    
    vector<vector<int>> g(x+1);
    
    for(int i: a){
        for(int j = i; j <= x; j+=i){
            g[j].push_back(j/i);
        }
    }
    
    for(int gg = 0; gg < min(s, 45); gg++){
        vector<int> ndp(x+1, 0);
        for(int i = x; i > y; i--){
            for(int j: g[i]) ndp[j] |= dp[i];
            for(int cc: b) {
                if(cc >= i) continue;
                int j = i-cc;
                ndp[j] |= dp[i]>>1;
            }
        }
    
        dp = ndp;
    }
    
    for(int i = x; i > -1; i--){
        if(i <= y && dp[i]) {
            cout << "Yes\n";
            return 0;
        }
        if(i <= mnt && dp[i] && g[i].size() == 0){
            cout << "Yes\n";
            return 0;
        }
        if(i > mnt && (dp[i] & 1) && g[i].size() == 0){
            cout << "Yes\n";
            return 0;
        }
    }

    cout << "No\n";

    return 0;
}

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr);

    int t; cin >> t;
    while(t--){
        solve();
    }


    return 0;
}