typedef long long ll;

string chars = "0124689";

bool isPrime(ll x){
    if(x < 2) return false;
    for(ll i = 2; i * i <= x; i++){
        if(x % i == 0) return false;
    }
    return true;
}

void dfs(string num){
    reverse(num.begin(), num.end());
    if(isPrime(stoll(num))) return;
    cout << num << endl;
    reverse(num.begin(), num.end());


    for(char c: chars){
        string ss = num;
        ss += c;
        dfs(ss);
    }

    return;
}

